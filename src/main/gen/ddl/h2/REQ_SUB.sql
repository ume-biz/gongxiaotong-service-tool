
DROP TABLE IF EXISTS `REQ_SUB` ;
CREATE TABLE `REQ_SUB` (
    `REQ_CODE` VARCHAR(32) NOT NULL	,
    `REQ_SUB_SEQ` INT NOT NULL	,
    `PRODUCT_CODE` VARCHAR(32) 	,
    `PRODUCT_DESC` VARCHAR(256) 	,
    `PRODUCT_SPECS` VARCHAR(64) 	,
    `QUANTITY` INT 	,
    `ESTIMATION` INT 	,
    `LOGISTICS_CODE` VARCHAR(128) 	,
    `LOGISTICS_DESC` VARCHAR(256) 	,
    `REQ_SUB_STATUS` INT 	,
    `CREATE_AUTHOR` VARCHAR(32),
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `UPDATE_AUTHOR` VARCHAR(32),
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP
);
ALTER TABLE `REQ_SUB` ADD CONSTRAINT `PK_REQ_SUB` PRIMARY KEY ( REQ_CODE ,REQ_SUB_SEQ ) ;
