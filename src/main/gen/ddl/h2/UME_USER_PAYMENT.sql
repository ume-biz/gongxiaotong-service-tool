
DROP TABLE IF EXISTS `UME_USER_PAYMENT` ;
CREATE TABLE `UME_USER_PAYMENT` (
    `USER_ID` VARCHAR(32) NOT NULL	,
    `SEQ` INT NOT NULL	,
    `CARD_TYPE` INT 	,
    `CARD_NUMBER` VARCHAR(128) 	,
    `CARD_OWNER` VARCHAR(32) 	,
    `CARD_BANK` VARCHAR(32) 	,
    `CARD_EXPIRATION_DATE` DATE 	,
    `CARD_STATUS` VARCHAR(8) 	,
    `CREATE_AUTHOR` VARCHAR(32),
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `UPDATE_AUTHOR` VARCHAR(32),
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP
);
ALTER TABLE `UME_USER_PAYMENT` ADD CONSTRAINT `PK_UME_USER_PAYMENT` PRIMARY KEY ( USER_ID ,SEQ ) ;
