
DROP TABLE IF EXISTS `REQ_MAIN` ;
CREATE TABLE `REQ_MAIN` (
    `REQ_CODE` VARCHAR(32) NOT NULL	,
    `REQ_DESC` VARCHAR(256) 	,
    `REQ_DELIVERY_DATE` DATE 	,
    `REQ_DELIVERY_ADDRESS` VARCHAR(256) NOT NULL	,
    `REQ_CONTACT_MOBILE` VARCHAR(16) 	,
    `REQ_TOTAL_ESTIMATION` INT 	,
    `REQ_FINAL_BID_CODE` VARCHAR(32) 	,
    `REQ_STATUS` INT NOT NULL	,
    `CREATE_AUTHOR` VARCHAR(32),
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `UPDATE_AUTHOR` VARCHAR(32),
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP
);
ALTER TABLE `REQ_MAIN` ADD CONSTRAINT `PK_REQ_MAIN` PRIMARY KEY ( REQ_CODE ) ;
