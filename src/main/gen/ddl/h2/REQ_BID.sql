
DROP TABLE IF EXISTS `REQ_BID` ;
CREATE TABLE `REQ_BID` (
    `REQ_CODE` VARCHAR(32) NOT NULL	,
    `BID_CODE` VARCHAR(32) NOT NULL	,
    `BID_DESC` VARCHAR(256) 	,
    `BID_PRICE` INT 	,
    `BID_OWNER` VARCHAR(128) 	,
    `BID_OWNER_MOBILE` VARCHAR(16) 	,
    `CREATE_AUTHOR` VARCHAR(32),
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    `UPDATE_AUTHOR` VARCHAR(32),
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP
);
ALTER TABLE `REQ_BID` ADD CONSTRAINT `PK_REQ_BID` PRIMARY KEY ( REQ_CODE ,BID_CODE ) ;
