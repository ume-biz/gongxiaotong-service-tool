
/* REQ_MAIN : 需求发布主信息表 */
DROP TABLE IF EXISTS `REQ_MAIN` ;
CREATE TABLE `REQ_MAIN` (
    `REQ_CODE` VARCHAR(32) BINARY NOT NULL	 COMMENT '需求编号',
    `REQ_DESC` VARCHAR(256) BINARY 	 COMMENT '需求描述',
    `REQ_DELIVERY_DATE` DATE 	 COMMENT '期望发货时间',
    `REQ_DELIVERY_ADDRESS` VARCHAR(256) BINARY NOT NULL	 COMMENT '配送地址',
    `REQ_CONTACT` VARCHAR(16) BINARY 	 COMMENT '联系人',
    `REQ_CONTACT_MOBILE` VARCHAR(16) BINARY 	 COMMENT '联系电话',
    `REQ_TOTAL_ESTIMATION` INT 	 COMMENT '需求总价估算',
    `REQ_FINAL_BID_CODE` VARCHAR(32) BINARY 	 COMMENT '最终报价编号',
    `REQ_STATUS` INT NOT NULL	 COMMENT '需求状态',
    `CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者',
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者',
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '需求发布主信息表' ;
ALTER TABLE `REQ_MAIN` ADD CONSTRAINT `PK_REQ_MAIN` PRIMARY KEY ( REQ_CODE ) ;
