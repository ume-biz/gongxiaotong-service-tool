
/* GXT_USER : 供销通用户表 */
DROP TABLE IF EXISTS `GXT_USER` ;
CREATE TABLE `GXT_USER` (
    `USER_ID` VARCHAR(16) BINARY NOT NULL	 COMMENT '用户识别ID',
    `USER_POST_ADDR` VARCHAR(256) BINARY 	 COMMENT '用户收货地址(默认)',
    `USER_POST_ADDR_OTHER` VARCHAR(1024) BINARY 	 COMMENT '用户收货地址(备用)',
    `USER_BANK_ACC_NO` VARCHAR(32) BINARY 	 COMMENT '用户银行账号(默认)',
    `USER_BANK_ACC_NO_OTHER` VARCHAR(128) BINARY 	 COMMENT '用户银行账号(备用)',
    `USER_BANK_ACC_NAME` VARCHAR(32) BINARY 	 COMMENT '用户银行名称(默认)',
    `USER_BANK_ACC_NAME_OTHER` VARCHAR(128) BINARY 	 COMMENT '用户银行名称(备用)',
    `USER_IDENTITY_CARD` VARCHAR(32) BINARY 	 COMMENT '用户身份证号码',
    `WECHAT_OPENID` VARCHAR(32) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_CITY` VARCHAR(64) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_COUNTRY` VARCHAR(64) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_HEADIMGURL` VARCHAR(512) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_LANGUAGE` VARCHAR(64) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_NICKNAME` VARCHAR(1024) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_PROVINCE` VARCHAR(64) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_SEX` VARCHAR(8) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_UNIONID` VARCHAR(64) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_PRIVILEGE` VARCHAR(20) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_GROUPID` INT 	 COMMENT '关联微信属性',
    `WECHAT_TAGID_LIST` VARCHAR(512) BINARY 	 COMMENT '关联微信属性',
    `WECHAT_SUBSCRIBE` INT 	 COMMENT '关联微信属性',
	`WECHAT_SUBSCRIBE_TIME` TIMESTAMP 	NULL DEFAULT NULL COMMENT '关联微信属性',
    `CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者',
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者',
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '供销通用户表' ;
ALTER TABLE `GXT_USER` ADD CONSTRAINT `PK_GXT_USER` PRIMARY KEY ( USER_ID ) ;
