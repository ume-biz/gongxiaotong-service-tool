
/* ORDER_ITEM_PROP : 需求发布明细信息表 */
DROP TABLE IF EXISTS `ORDER_ITEM_PROP` ;
CREATE TABLE `ORDER_ITEM_PROP` (
    `ORDER_CODE` VARCHAR(32) BINARY NOT NULL	 COMMENT '需求编号',
    `ORDER_ITEM_CODE` VARCHAR(32) BINARY 	 COMMENT '品类编号',
    `ORDER_ITEM_PROP_GROUP` VARCHAR(256) BINARY 	 COMMENT '品类项目属性分组',
    `ORDER_ITEM_PROP_KEY` VARCHAR(256) BINARY 	 COMMENT '品类项目属性KEY',
    `ORDER_ITEM_PROP_VALUE` VARCHAR(64) BINARY 	 COMMENT '品类项目属性VALUE',
    `ORDER_ITEM_PROP_OPTION` INT 	 COMMENT '品类项目属性可选标识',
    `CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者',
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者',
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '需求发布明细信息表' ;
ALTER TABLE `ORDER_ITEM_PROP` ADD CONSTRAINT `PK_ORDER_ITEM_PROP` PRIMARY KEY ( ORDER_CODE ) ;

DROP TABLE IF EXISTS `ORDER_ITEM_PROP_HT` ;
CREATE TABLE `ORDER_ITEM_PROP_HT` (
    `OPERATION_HISTORY_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '操作时间',
    `OPERATION_HISTORY_CATEGORY` CHAR(1) COMMENT '操作类别',
    `ORDER_CODE` VARCHAR(32) COMMENT '需求编号',
    `ORDER_ITEM_CODE` VARCHAR(32) COMMENT '品类编号',
    `ORDER_ITEM_PROP_GROUP` VARCHAR(256) COMMENT '品类项目属性分组',
    `ORDER_ITEM_PROP_KEY` VARCHAR(256) COMMENT '品类项目属性KEY',
    `ORDER_ITEM_PROP_VALUE` VARCHAR(64) COMMENT '品类项目属性VALUE',
    `ORDER_ITEM_PROP_OPTION` INT COMMENT '品类项目属性可选标识',
    `CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者',
    `CREATE_DATETIME` TIMESTAMP COMMENT '创建时间',
    `UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者',
    `UPDATE_DATETIME` TIMESTAMP COMMENT '更新时间'
);
