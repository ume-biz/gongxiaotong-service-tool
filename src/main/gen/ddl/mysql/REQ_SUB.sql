
/* REQ_SUB : 需求发布明细信息表 */
DROP TABLE IF EXISTS `REQ_SUB` ;
CREATE TABLE `REQ_SUB` (
    `REQ_CODE` VARCHAR(32) BINARY NOT NULL	 COMMENT '需求编号',
    `REQ_SUB_SEQ` INT NOT NULL	 COMMENT '品类编号',
    `PRODUCT_CODE` VARCHAR(32) BINARY 	 COMMENT '品类编号',
    `PRODUCT_DESC` VARCHAR(256) BINARY 	 COMMENT '品类描述',
    `PRODUCT_SPECS` VARCHAR(64) BINARY 	 COMMENT '规格',
    `QUANTITY` INT 	 COMMENT '数量',
    `ESTIMATION` INT 	 COMMENT '期望单价(MAX)',
    `LOGISTICS_CODE` VARCHAR(128) BINARY 	 COMMENT '物流单号',
    `LOGISTICS_DESC` VARCHAR(256) BINARY 	 COMMENT '物流描述/动态',
    `REQ_SUB_STATUS` INT 	 COMMENT '物流签收状态',
    `CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者',
    `CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者',
    `UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '需求发布明细信息表' ;
ALTER TABLE `REQ_SUB` ADD CONSTRAINT `PK_REQ_SUB` PRIMARY KEY ( REQ_CODE ,REQ_SUB_SEQ ) ;
